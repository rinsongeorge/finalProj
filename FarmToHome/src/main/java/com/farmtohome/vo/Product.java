package com.farmtohome.vo;

/**
 * @author rinson
 *
 */
public class Product {

	private String productID;
	private String productName;
	private String productUnitPrice;
	private String currency;
	private String productUnitWeight;
	private String productDescription;
	private String productImage;
	private String productThumbImage;
	private String categoryID;
	
	public String getProductID() {
		return productID;
	}
	public void setProductID(String productID) {
		this.productID = productID;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductUnitPrice() {
		return productUnitPrice;
	}
	public void setProductUnitPrice(String productUnitPrice) {
		this.productUnitPrice = productUnitPrice;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public String getProductUnitWeight() {
		return productUnitWeight;
	}
	public void setProductUnitWeight(String productUnitWeight) {
		this.productUnitWeight = productUnitWeight;
	}
	public String getProductDescription() {
		return productDescription;
	}
	public void setProductDescription(String productDescription) {
		this.productDescription = productDescription;
	}
	public String getProductImage() {
		return productImage;
	}
	public void setProductImage(String productImage) {
		this.productImage = productImage;
	}
	public String getProductThumbImage() {
		return productThumbImage;
	}
	public void setProductThumbImage(String productThumbImage) {
		this.productThumbImage = productThumbImage;
	}
	public String getCategoryID() {
		return categoryID;
	}
	public void setCategoryID(String categoryID) {
		this.categoryID = categoryID;
	}
	
}
